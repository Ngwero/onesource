export function applyProductTermRules(segment, lang) {
  const trimmed = segment.trim();
  if (!trimmed) return trimmed;

  const common = COMMON_PHRASES[trimmed];
  const langKey = lang;
  if (common?.[langKey]) return common[langKey];

  let out = trimmed;
  const rules = WORD_RULES[langKey] ?? [];
  for (const [re, repl] of rules) {
    out = out.replace(re, repl);
  }
  return out;
}

/** Used by scripts/generate-product-terms.mjs */
export function translateSegment(segment, lang) {
  return applyProductTermRules(segment, lang);
}
